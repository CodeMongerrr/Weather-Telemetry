/** Generates a global lat/lon grid at 8Â° resolution (966 points). */
function buildGrid(): Location[] {
  const points: Location[] = [];
  for (let lat = -80; lat <= 80; lat += 8) {
    for (let lon = -180; lon <= 180; lon += 8) {
      const ns = lat >= 0 ? "N" : "S";
      const ew = lon >= 0 ? "E" : "W";
      points.push({
        city_name: `Grid_${Math.abs(lat).toString().padStart(2, "0")}${ns}_${Math.abs(lon).toString().padStart(3, "0")}${ew}`,
        latitude: lat,
        longitude: lon,
      });
    }
  }
  return points;
}

